const out = ["// Generated by scripts/gen-icons.mjs — do not edit by hand.",
  "// Icon artwork: Material Design Icons (@material-design-icons/svg), Apache-2.0.",
  "export const ICON_PATHS = {"];

for (const [name, style] of Object.entries(ICONS)) {
  const svg = readFileSync(
    new URL(`../node_modules/@material-design-icons/svg/${style}/${name}.svg`, import.meta.url),
    "utf8",
  );
  const paths = [...svg.matchAll(/<path([^>]*)\/>/g)]
    .filter(([, attrs]) => !attrs.includes('fill="none"'))
    .map(([, attrs]) => attrs.match(/ d="([^"]+)"/)?.[1])
    .filter(Boolean);
  if (paths.length === 0) throw new Error(`no path data in ${style}/${name}.svg`);
  out.push(`  ${name}: ${JSON.stringify(paths.join(" "))},`);
}

out.push("} as const;", "", "export type IconName = keyof typeof ICON_PATHS;", "");
writeFileSync(new URL("../src/components/iconPaths.ts", import.meta.url), out.join("\n"));
